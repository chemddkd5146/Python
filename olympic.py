import turtle as t

t.shape("turtle")
t.speed(20)
t.pensize(20)

t.circle(100)

t.up()
t.forward(240)
t.down()
t.pencolor('red')
t.circle(100)

t.up()
t.backward(480)
t.down()
t.pencolor('blue')
t.circle(100)

t.up()
t.right(40)
t.forward(80)
t.down()
t.pencolor('yellow')
t.circle(100)

t.up()
t.left(35)
t.forward(300)
t.down()
t.pencolor('green')
t.circle(100)

t.done()